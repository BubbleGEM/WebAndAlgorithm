function InsertionSort(array: number[]): number[] {
  for (let index = 1; index < array.length; index++) {
    let i: number = index
    const temp = array[index]
    while (i > 0 && array[i - 1] > temp) {
      array[i] = array[i - 1]
      i--
    }
    array[i] = temp
  }
  return array
}

